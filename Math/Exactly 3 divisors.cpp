bool isprime(int n){
  if(n==1)return false;
   for(int i=2;i*i<=n;i++){
     if(n%i==0){
       return false;
       break;
     }
   }
   return true;
}

int exactly3Divisors(int n){
  int count=0;
  for(int i=1;i*i<=n;i++){
   if(isprime(i)){
     if(i*i<=n){
       cout<<i*i<<" ";
     count++;
     }
   }
  }
  return count;
}
